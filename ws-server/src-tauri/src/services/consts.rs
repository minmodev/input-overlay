pub fn mouse_button_name(btn: u8) -> Option<&'static str> {
    match btn {
        1 => Some("mouse_left"),
        2 => Some("mouse_right"),
        3 => Some("mouse_middle"),
        4 => Some("mouse_4"),
        5 => Some("mouse_5"),
        _ => None,
    }
}

pub fn mouse_scroll_name(rot: i8) -> Option<&'static str> {
    match rot {
        r if r > 0 => Some("mouse_scroll_up"),
        r if r < 0 => Some("mouse_scroll_down"),
        _ => None,
    }
}
